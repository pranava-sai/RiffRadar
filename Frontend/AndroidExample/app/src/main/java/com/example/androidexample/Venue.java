package com.example.androidexample;

/**
 * The Venue class represents a venue entity, extending the User class.
 */
public class Venue extends User {

    /** The image of the venue. */
    public String image;

    /** The description of the venue. */
    public String description;

    /** The capacity of the venue. */
    public String capacity;

    /** The address of the venue in one line. */
    public String addressOneLine;

    /** The detailed address of the venue. */
    public Address address;

    /** The genre of the venue. */
    public String genre;

}
